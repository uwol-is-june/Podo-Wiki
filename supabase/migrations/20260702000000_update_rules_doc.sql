## 편집 규칙

### 내용

- **사실에 기반한 내용**만 작성합니다. 개인적인 의견이나 추측은 배제합니다.
- 다른 단체나 개인을 **비하하거나 허위사실**을 작성하지 않습니다.
- 특정인의 **개인정보**(실명, 연락처, 주소 등)를 무단으로 게재하지 않습니다.
- **저작권**이 있는 자료를 출처 없이 무단으로 사용하지 않습니다.

### 언어

- 정중하고 객관적인 문체를 사용합니다.
- 비속어, 욕설, 혐오 표현을 사용하지 않습니다.

