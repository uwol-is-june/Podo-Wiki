## 편집 방법

포도위키는 **마크다운을 직접 입력할 필요 없이**, 문서 상단 **수정** 탭을 누르면 나타나는 편집기 툴바로 서식을 지정합니다. 원하는 부분을 선택한 뒤 툴바 버튼을 누르면 바로 적용됩니다.

### 글자 서식

- **굵게**(Ctrl+B), *기울임*(Ctrl+I), 취소선, 인라인 코드를 지원합니다.
- 글자색은 빨강·파랑·초록·주황·보라 중에서 고르거나 초기화(✕)할 수 있습니다.

### 문서 구조

- **제목(H1·H2·H3)**: 제목을 지정하면 문서 오른쪽에 **목차가 자동으로 생성**되고, 제목마다 번호가 매겨집니다. 긴 문서일수록 제목으로 구조를 나눠 주세요.
- **목록**: 글머리 기호 목록(•)과 번호 매기기 목록(1.)을 사용할 수 있습니다.
- **인용**, **코드 블록**, **구분선**으로 내용을 구분합니다.

### 링크·이미지·표

- **링크**: 버튼을 누르면 표시할 텍스트와 주소를 입력하는 창이 뜹니다. 내부 문서는 `/w/문서제목` 형식으로 연결합니다.
- **이미지**: 파일을 선택하면 업로드되어 문서에 삽입되며, 삽입한 이미지는 모서리를 끌어 크기를 조절할 수 있습니다.
- **표**: 표를 삽입한 뒤 표 안에 커서를 두면 행·열을 추가하거나 삭제하는 버튼이 나타납니다.

### 각주

각주 버튼을 누르면 본문에는 참조 번호가, 문서 맨 아래에는 각주 내용이 추가됩니다. 번호는 본문 순서대로 **자동으로 매겨지며**, 각주를 지우거나 순서가 바뀌면 다시 정렬됩니다. 본문의 각주 번호를 클릭하면 내용 보기·수정·삭제를 할 수 있습니다.

### 저장

- 편집을 마치면 아래쪽 **편집 요약**(무엇을 바꿨는지)을 적고 **저장**을 누릅니다. 편집 요약은 역사에 함께 기록됩니다.
- 한 문서를 편집하는 동안에는 다른 사람이 동시에 수정하지 못하도록 **편집 잠금**이 걸리며, 편집을 끝내거나 취소하면 풀립니다.
- 잘못 저장했다면 문서 상단 **역사** 탭에서 이전 버전을 확인하고 되돌릴 수 있습니다.

