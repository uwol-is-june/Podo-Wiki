## 공연권 허락은 어떻게 받나요?

공연하려는 작품의 **저작권자에게 사전에 공연권 사용 허가**를 받아야 합니다.

1. 작품의 저작권자(작가·번역가·작곡가) 또는 저작권을 관리하는 대리 기관을 확인합니다.
2. 공연 일정, 장소, 횟수, 유·무료 여부를 정리해 서면(이메일)으로 허가를 요청합니다.
3. 허가 조건(사용료, 크레딧 표기 등)을 확인하고 **서면으로 답변을 보관**합니다.

자세한 절차나 단체별 경험은 각 단체의 인수인계 문서를 참고해 주세요.

## 회원가입과 승인은 어떻게 되나요?

1. 상단 **로그인** → 회원가입에서 단체명과 이름을 입력해 가입합니다.
2. 관리자가 확인 후 승인하면 문서를 작성·수정할 수 있습니다.
3. 승인 전에도 모든 문서를 자유롭게 열람할 수 있습니다.

## 문서는 누구나 수정할 수 있나요?

열람은 누구나 가능하지만, **수정은 승인된 회원**만 할 수 있습니다.
단, FAQ·도움말 등 일부 운영 문서는 보호 문서로 지정되어 있어 관리자만 수정할 수 있습니다.

## 인수인계 문서는 어떻게 만드나요?

`/w/단체명/파트명` 주소로 접속하면 새 문서를 만들 수 있습니다.
문서 구조 예시와 작성 요령은 [포도위키:도움말](/w/포도위키:도움말)을 참고해 주세요.

## 잘못 편집했을 때는 어떻게 하나요?

문서 상단 **역사** 탭에서 이전 버전을 확인할 수 있습니다.
되돌리기가 필요하면 관리자에게 문의해 주세요.

