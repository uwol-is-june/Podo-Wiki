## 인수인계 문서 예시 구조

아래 틀을 복사해 시작하면 좋습니다.

```
# 단체명/파트명 인수인계

## 역할 개요
이 역할이 하는 일 요약

## 정기 업무
시즌 중 반복되는 업무 목록

## 장비 및 시스템
사용하는 장비나 시스템 설명

## 주의사항
반드시 알아야 할 사항

## 인수인계 체크리스트
- [ ] 다음 담당자가 확인해야 할 항목
```

