## 포도위키란?

포도위키는 **공연단체 간 인수인계를 원활하게** 하기 위한 위키입니다.
개인 블로그나 일기가 아닌, 다음 담당자가 바로 업무를 이어받을 수 있도록 돕는 실용 문서를 지향합니다.

- **읽기**는 누구나 자유롭게 할 수 있습니다.
- **편집**은 회원가입 후 관리자 승인을 받은 회원만 가능합니다.

이 문서는 포도위키를 사용할 때 알아야 할 이용 목적, 문서 작성 규칙, 에디터 사용법을 한곳에 모아둔 안내서입니다.

---

## 시작하기

### 회원가입 및 승인 절차

1. 상단 **로그인** → 회원가입으로 이동합니다.
2. 단체명과 이름을 입력해 가입합니다.
3. 관리자가 승인하면 편집 권한이 부여됩니다. 승인 전에는 문서를 읽을 수만 있습니다.

### 문서 주소와 검색

- 문서 주소 형식: `/w/문서제목` (예: `/w/홍길동단/무대팀`)
- 상단 검색창에 키워드를 입력하면 문서 제목으로 검색할 수 있습니다.

---

## 문서 작성 규칙

### 문서 제목

제목은 검색하기 쉽고 일관성 있게 작성합니다.

- **단체/부서 문서**: `단체명/파트명` 형식을 권장합니다.
  - 예) `홍길동단/무대팀`, `한국공연단/음향`
- **공통 정보**: 특정 단체에 국한되지 않는 정보는 일반 명사로 작성합니다.
  - 예) `조명 장비 사용법`, `공연 준비 체크리스트`

### 꼭 포함하면 좋은 내용

- 해당 **시즌 또는 연도**
- **담당자 역할** (실명 대신 역할명 권장)
- **장비·공간·시스템** 사용 방법
- **주의사항** 및 자주 하는 실수
- 연락해야 할 곳이나 사람의 역할명

### 지켜야 할 원칙

- **사실에 기반한 내용**만 작성합니다. 개인적인 의견이나 추측은 배제합니다.
- 다른 단체나 개인을 **비하하거나 허위사실**을 작성하지 않습니다.
- 특정인의 **개인정보**(실명, 연락처, 주소 등)를 무단으로 게재하지 않습니다.
- **저작권**이 있는 자료를 출처 없이 무단으로 사용하지 않습니다.
- 정중하고 객관적인 문체를 사용하고, 비속어·욕설·혐오 표현을 쓰지 않습니다.

