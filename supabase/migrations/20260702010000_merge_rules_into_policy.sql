UPDATE documents
SET content = REPLACE(content, '[포도위키:규칙](/w/포도위키:규칙)', '[포도위키:편집방침](/w/포도위키:편집방침)')
WHERE slug = '포도위키:도움말'
  AND content LIKE '%[포도위키:규칙](/w/포도위키:규칙)%';

-- 통합으로 더 이상 쓰지 않는 규칙 문서 삭제
DELETE FROM documents WHERE slug = '포도위키:규칙';
